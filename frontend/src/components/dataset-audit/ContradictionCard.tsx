import { useEffect, useRef } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { ChevronDown } from 'lucide-react';
import type { ContradictoryLabelResult } from '../../types/dataset_intelligence';
import { useRDKit } from '../../hooks/useRDKit';
import { ClayCard } from '../ui/ClayCard';

// =============================================================================
// Types
// =============================================================================

interface ContradictionCardProps {
  /** Contradictory label result for a single InChIKey. */
  contradiction: ContradictoryLabelResult;
  /** Whether this card's entries are expanded. */
  isExpanded: boolean;
  /** Toggle expand/collapse. */
  onToggle: () => void;
}

// =============================================================================
// Helpers
// =============================================================================

/**
 * Get fold-difference badge color classes based on severity.
 * >= 100x: red (severe), >= 10x: amber (significant), >= 3x: yellow (moderate).
 */
function getFoldBadgeClasses(fold: number): string {
  if (fold >= 100) {
    return 'bg-red-100 text-red-700 dark:bg-red-900/30 dark:text-red-400';
  }
  if (fold >= 10) {
    return 'bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-400';
  }
  return 'bg-yellow-100 text-yellow-700 dark:bg-yellow-900/30 dark:text-yellow-400';
}

/**
 * Truncate an InChIKey in the middle for display.
 * "ABCDEFGHIJKLMNOP-QRSTUVWXYZ" => "ABCDEF...UVWXYZ"
 */
function truncateInChIKey(key: string): string {
  if (key.length <= 16) return key;
  return `${key.slice(0, 8)}...${key.slice(-6)}`;
}

// =============================================================================
// 2D Structure renderer
// =============================================================================

/**
 * Renders a 2D molecular structure via RDKit.js SVG at 120x100px.
 *
 * Uses the established RDKit.js pattern from the Compound Profiler (Phase 7).
 * The SVG is generated by RDKit.js WASM module -- a trusted local library,
 * not user-supplied HTML content.
 */
function StructureSVG({ smiles }: { smiles: string }) {
  const { rdkit } = useRDKit();
  const containerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!rdkit || !containerRef.current || !smiles) return;

    const mol = rdkit.get_mol(smiles);
    if (mol) {
      try {
        const svg = mol.get_svg(120, 100);
        // RDKit.js WASM produces trusted SVG -- same pattern as ProfilerInput, HeroSection
        containerRef.current.replaceChildren();
        const wrapper = document.createElement('div');
        wrapper.innerHTML = svg; // eslint-disable-line -- trusted RDKit SVG output
        if (wrapper.firstChild) {
          containerRef.current.appendChild(wrapper.firstChild);
        }
      } finally {
        mol.delete();
      }
    } else {
      containerRef.current.textContent = 'Invalid';
    }
  }, [rdkit, smiles]);

  return (
    <div
      ref={containerRef}
      className="w-[120px] h-[100px] flex items-center justify-center shrink-0 text-xs text-[var(--color-text-muted)]"
      aria-hidden="true"
    />
  );
}

// =============================================================================
// Component
// =============================================================================

/**
 * Card showing a contradictory label group for a single InChIKey.
 *
 * Layout: horizontal split with 2D structure on the left and metadata on the right.
 * Expandable to show all entries for the InChIKey with Framer Motion animation.
 * Fold-difference badge colored by severity per UI-SPEC.
 */
export function ContradictionCard({
  contradiction,
  isExpanded,
  onToggle,
}: ContradictionCardProps) {
  const { inchikey, fold_difference, entry_count, smiles, entries } =
    contradiction;

  return (
    <ClayCard
      size="sm"
      hover
      className="cursor-pointer"
      onClick={onToggle}
      aria-expanded={isExpanded}
      aria-label={`${inchikey}: ${fold_difference.toFixed(1)}x fold-difference, ${entry_count} entries`}
    >
      {/* Main row: structure + metadata */}
      <div className="flex items-start gap-4">
        {/* Left: 2D structure */}
        <StructureSVG smiles={smiles} />

        {/* Right: metadata */}
        <div className="flex-1 min-w-0">
          {/* InChIKey */}
          <p
            className="text-xs font-mono text-[var(--color-text-secondary)] truncate"
            title={inchikey}
          >
            {truncateInChIKey(inchikey)}
          </p>

          {/* Fold-difference badge + entry count */}
          <div className="flex items-center gap-3 mt-2">
            <span
              className={`inline-flex items-center px-2 py-0.5 text-sm font-semibold rounded-full ${getFoldBadgeClasses(fold_difference)}`}
            >
              {fold_difference.toFixed(1)}x
            </span>
            <span className="text-xs text-[var(--color-text-muted)]">
              {entry_count} entries
            </span>
          </div>

          {/* Expand indicator */}
          <div className="flex items-center gap-1 mt-2 text-xs text-[var(--color-text-muted)]">
            <motion.span
              animate={{ rotate: isExpanded ? 180 : 0 }}
              transition={{ duration: 0.25, ease: 'easeOut' }}
            >
              <ChevronDown className="w-3.5 h-3.5" />
            </motion.span>
            <span>{isExpanded ? 'Hide entries' : 'Show entries'}</span>
          </div>
        </div>
      </div>

      {/* Expandable entries section */}
      <AnimatePresence initial={false}>
        {isExpanded && (
          <motion.div
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: 'auto', opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={{ duration: 0.25, ease: 'easeOut' }}
            className="overflow-hidden"
          >
            <div className="mt-4 pt-3 border-t border-[var(--color-border)]">
              <table className="w-full text-xs">
                <thead>
                  <tr className="text-[var(--color-text-secondary)]">
                    <th className="text-left py-1 pr-3 font-medium">Row</th>
                    <th className="text-left py-1 pr-3 font-medium">SMILES</th>
                    <th className="text-right py-1 font-medium">Activity</th>
                  </tr>
                </thead>
                <tbody>
                  {entries.map((entry) => (
                    <tr
                      key={entry.row_index}
                      className="border-t border-[var(--color-border)]/50"
                    >
                      <td className="py-1.5 pr-3 text-[var(--color-text-muted)] tabular-nums">
                        {entry.row_index}
                      </td>
                      <td
                        className="py-1.5 pr-3 font-mono text-[var(--color-text-primary)] truncate max-w-[200px]"
                        title={entry.smiles}
                      >
                        {entry.smiles}
                      </td>
                      <td className="py-1.5 text-right font-mono text-[var(--color-text-primary)] tabular-nums">
                        {typeof entry.activity === 'number'
                          ? entry.activity.toPrecision(4)
                          : String(entry.activity)}
                      </td>
                    </tr>
                  ))}
                </tbody>
              </table>
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </ClayCard>
  );
}
